def add_styled_paragraph(doc, text, style='Normal', bold=False, italic=False, font_size=None, alignment=None, space_before=None, space_after=None):
    """Добавить стилизованный параграф"""
    p = doc.add_paragraph(text, style=style)
    for run in p.runs:
        if bold:
            run.bold = True
        if italic:
            run.italic = True
        if font_size:
            run.font.size = Pt(font_size)
        run.font.name = 'Times New Roman'
    if alignment:
        p.alignment = alignment
    if space_before:
        p.paragraph_format.space_before = Pt(space_before)
    if space_after:
        p.paragraph_format.space_after = Pt(space_after)
    return p

def add_heading_styled(doc, text, level=1):
    """Добавить заголовок с правильным стилем"""
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.name = 'Times New Roman'
        run.font.color.rgb = RGBColor(0, 0, 0)
    return h

def add_bullet(doc, text):
    """Добавить пункт списка с маркером"""
    p = doc.add_paragraph()
    run = p.add_run('• ' + text)
    run.font.size = Pt(12)
    run.font.name = 'Times New Roman'
    p.paragraph_format.left_indent = Cm(1.5)
    p.paragraph_format.first_line_indent = Cm(-0.5)
    return p

def add_numbered(doc, text, number):
    """Добавить нумерованный пункт"""
    p = doc.add_paragraph()
    run = p.add_run(f'{number}. {text}')
    run.font.size = Pt(12)
    run.font.name = 'Times New Roman'
    p.paragraph_format.left_indent = Cm(1.5)
    p.paragraph_format.first_line_indent = Cm(-0.75)
    return p

def add_table_with_data(doc, headers, data, caption=None):
    """Добавить таблицу с данными"""
    if caption:
        p = doc.add_paragraph(caption)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        for run in p.runs:
            run.font.size = Pt(12)
            run.font.name = 'Times New Roman'
    
    table = doc.add_table(rows=1+len(data), cols=len(headers))
    # Не устанавливаем стиль, если его нет в документе
    try:
        table.style = 'Table Grid'
    except:
        pass
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    
    # Заголовки
    for i, header in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = header
        for paragraph in cell.paragraphs:
            paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in paragraph.runs:
                run.bold = True
                run.font.size = Pt(12)
                run.font.name = 'Times New Roman'
    
    # Данные
    for row_idx, row_data in enumerate(data):
        for col_idx, cell_text in enumerate(row_data):
            cell = table.rows[row_idx+1].cells[col_idx]
            cell.text = str(cell_text)
            for paragraph in cell.paragraphs:
                for run in paragraph.runs:
                    run.font.size = Pt(12)
                    run.font.name = 'Times New Roman'
    
    # Отступ после таблицы
    doc.add_paragraph()
    return table

def set_cell_shading(cell, color):
    """Установить цвет ячейки"""
    shading_elm = cell._element.get_or_add_tcPr()
    shading = shading_elm.makeelement(qn('w:shd'), {
        qn('w:fill'): color,
        qn('w:val'): 'clear'
    })
    shading_elm.append(shading)

