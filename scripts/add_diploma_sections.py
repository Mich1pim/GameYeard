def set_cell_shading(cell, color):
    """Установка цвета фона ячейки таблицы"""
    shading = cell._element.get_or_add_tcPr()
    shading_elem = Document().element.makeelement(
        '{http://schemas.openxmlformats.org/wordprocessingml/2006/main}shd',
        {
            '{http://schemas.openxmlformats.org/wordprocessingml/2006/main}fill': color,
            '{http://schemas.openxmlformats.org/wordprocessingml/2006/main}val': 'clear'
        }
    )
    shading.append(shading_elem)

def add_styled_table(doc, headers, data, col_widths=None):
    """Добавление стилизованной таблицы"""
    table = doc.add_table(rows=1 + len(data), cols=len(headers))
    table.style = 'Table Grid'
    
    # Заголовки
    for i, header in enumerate(headers):
        cell = table.rows[0].cells[i]
        cell.text = header
        for paragraph in cell.paragraphs:
            paragraph.alignment = WD_ALIGN_PARAGRAPH.CENTER
            for run in paragraph.runs:
                run.bold = True
                run.font.size = Pt(12)
        set_cell_shading(cell, "D9E2F3")
    
    # Данные
    for row_idx, row_data in enumerate(data):
        for col_idx, cell_data in enumerate(row_data):
            cell = table.rows[row_idx + 1].cells[col_idx]
            cell.text = str(cell_data)
            for paragraph in cell.paragraphs:
                for run in paragraph.runs:
                    run.font.size = Pt(11)
    
    # Ширина колонок
    if col_widths:
        for row in table.rows:
            for idx, width in enumerate(col_widths):
                row.cells[idx].width = Inches(width)
    
    return table

